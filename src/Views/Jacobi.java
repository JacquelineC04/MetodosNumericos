    private void BtnRespuestaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnRespuestaActionPerformed
        // TODO add your handling code here:
        A= new double[n][n];
        B = new double[n];


        if ((MatrizPrincipalLlenar() && MatrizRespLlenar())) {
            for (int i = 0; i < n; i++) {
            for (int j = 0; j < n; j++) {
                A[i][j] = Double.parseDouble(tblMatriz.getValueAt(i, j).toString());
            }
                B[i] = Double.parseDouble(Respuesta.getValueAt(i, 0).toString());
        }
        }
    }//GEN-LAST:event_BtnRespuestaActionPerformed

