    private void btnCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCrearActionPerformed
        n = Integer.parseInt(txtN.getText());
        if (n > 7) {
            JOptionPane.showMessageDialog(rootPane, "No se puede generar una matriz de orden superior al 7");
        }else{
        int[][] matriz = new int [n][n];
        
        this.mostrarMatriz(matriz, n);
        this.mostrarRepuesta(matriz, n);
        this.MatrizTbl1(matriz, n);
        this.MatrizTbl2(matriz, n);
        this.MatrizTbl3(matriz, n);
        this.MatrizTbl4(matriz, n);
        this.MatrizTbl5(matriz, n);
        this.MatrizTbl6(matriz, n);
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_btnCrearActionPerformed

